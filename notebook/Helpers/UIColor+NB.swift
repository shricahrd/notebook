import Foundation
import UIKit

extension UIColor{
    
    class func NBtabBarBGColor() -> UIColor{
        return UIColor(red: 18/255, green: 18/255, blue: 18/255, alpha: 1)
    }
    
    class func NBNavBarBGColor() -> UIColor{
        return UIColor(red: 18/255, green: 18/255, blue: 18/255, alpha: 1)
    }
    
    class func NBMainColor() -> UIColor{
        return UIColor(red: 26/255, green: 26/255, blue: 26/255, alpha: 1)
    }
    
    class func NBGreenColor() -> UIColor{
        return UIColor(red: 69/255, green: 146/255, blue: 50/255, alpha: 1)
    }
    
    class func NBGradientEndColor() -> UIColor{
        return UIColor(red: 26/255, green: 80/255, blue: 110/255, alpha: 1)
    }
    
    class func NBGradientStartColor() -> UIColor{
        return UIColor(red: 150/255, green: 150/255, blue: 150/255, alpha: 1)
    }
    
    class func NBGoldColor() -> UIColor{
        return UIColor(red: 222/255, green: 187/255, blue: 95/255, alpha: 1)
    }
    
}
